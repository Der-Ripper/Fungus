package com.example.fungus.ui.Bluetooth;

public class BluetoothElement {
    private String name;
    private String address;

    public BluetoothElement(String name, String protection) {
        this.name = name;
        this.address = protection;
    }

    public String getName() {
        return name;
    }

    public String getAddress() {
        return address;
    }


}
